// 1. Variables y tipos
let myName = "Jose";
const birthYear = 1996;
let isDeveloper = true;

console.log("Nombre:", myName);
console.log("Año de nacimiento:", birthYear);
console.log("Es desarrollador:", isDeveloper);

// 2. Operaciones
let age = 2025 - birthYear;
console.log("Edad:", age);

let sum = 10 + 5;
let multiply = 10 * 3;
console.log("Suma:", sum, "Multiplicación:", multiply);

// 3. Comparaciones
console.log("¿Es mayor de 18?", age > 18);
console.log("¿Edad igual a 30?", age === 30);

// 4. Manipulación de strings
let greeting = `Hola, mi nombre es ${myName} y tengo ${age} años.`;
console.log(greeting);
