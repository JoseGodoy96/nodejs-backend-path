// Operadores aritmeticos
let num1 = 12;
let num2 = 6;

console.log("Es una suma", num1 + num2);
console.log("Es una resta", num1 - num2);
console.log("Es una multiplicacion", num1 * num2);
console.log("Es una division", num1 / num2);
console.log("Es una resto", num1 % num2);

// Operadores logicos

let verdad = true;
let falso = false;

console.log("Verdad y verdad", verdad && falso);
console.log("Verdad o falso", verdad || falso);
console.log("No es verdad", !verdad);

// Operadores de comparacion

let value1 = 5;
let value2 = "5";

console.log("Compara solo el valor", value1 == value2);
console.log("Compara valor y tipo", value1 === value2);
console.log("Distinto en valor", value1 != value2);
console.log("Distinto en valor o tipo", value1 !== value2);
console.log("Mayor que", num2 > value1);
console.log("Menor que", num2 < value1);
